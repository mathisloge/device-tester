//! from:
//! https://caedesnotes.wordpress.com/2015/04/13/how-to-integrate-your-sdl2-window-icon-or-any-image-into-your-executable/
void setWindowIcon(SDL_Window* window)
{
    // these masks are needed to tell SDL_CreateRGBSurface(From)
    // to assume the data it gets is byte-wise RGB(A) data
    uint32_t rmask, gmask, bmask, amask;
#if SDL_BYTEORDER == SDL_BIG_ENDIAN
    int shift = (gimp_image.bytes_per_pixel == 3) ? 8 : 0;
    rmask = 0xff000000 >> shift;
    gmask = 0x00ff0000 >> shift;
    bmask = 0x0000ff00 >> shift;
    amask = 0x000000ff >> shift;
#else // little endian, like x86
    rmask = 0x000000ff;
    gmask = 0x0000ff00;
    bmask = 0x00ff0000;
    amask = (gimp_image.bytes_per_pixel == 3) ? 0 : 0xff000000;
#endif
    SDL_Surface *icon = SDL_CreateRGBSurfaceFrom((void *)gimp_image.pixel_data,
                                                 gimp_image.width,
                                                 gimp_image.height,
                                                 gimp_image.bytes_per_pixel * 8,
                                                 gimp_image.bytes_per_pixel * gimp_image.width,
                                                 rmask,
                                                 gmask,
                                                 bmask,
                                                 amask);
    SDL_SetWindowIcon(window, icon);

    SDL_FreeSurface(icon);
}
